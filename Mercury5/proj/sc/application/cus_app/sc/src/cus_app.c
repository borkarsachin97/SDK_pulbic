/**u32 *cus_ApmStackBase(void);
 * brief:  query the minimum address of custom task stack
 */
u32 *cus_ApmStackBase(void)
{
	static u32 *miniAddr = (u32*)0xFFFFFFFF;
	u32 i;

	if((u32*)0xFFFFFFFF == miniAddr)
	{
		for (i =0 ; i < cus_apmNbApplicationTasks; i++)
		{
			if( (cus_apmTasks[i].CustomStackSize > CUS_DUMMY_TASK_STACK_SIZE)
			 && (cus_apmTasks[i].pCustomStack)
			 && (cus_apmTasks[i].pCustomStack < miniAddr) )
			{
				miniAddr = cus_apmTasks[i].pCustomStack;
			}
		}
		
	#ifdef __SDK_API__
		for (i=0; i<cus_sdkNbApplicationTasks; i++)
		{
			if( (cus_sdkTasks[i].CustomStackSize > CUS_DUMMY_TASK_STACK_SIZE)
			 && (cus_sdkTasks[i].pCustomStack)
			 && (cus_sdkTasks[i].pCustomStack < miniAddr) )
			{
				miniAddr = cus_sdkTasks[i].pCustomStack;
			}
		}
	#endif
	}
	
	return (miniAddr);
}

