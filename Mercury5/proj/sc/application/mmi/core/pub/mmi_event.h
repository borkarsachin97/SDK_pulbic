/**
* @file mmi_event.h
*
* This file defines the global events used in MMI implementation.
*
* Events used in MAE platform are 32-bit values. The higher 16-bit field denotes the class or the
* category that event belongs to. Most global (system) events are classified to special categories
* which are only dummy classes. Generally these global events are translated from system messages
* in the interface functions to drive the service components running on MAE platform.
*
* @version $Id: mmi_event.h 41649 2009-09-29 02:45:43Z christoph.kuo $
*/

